package com.metricstream.systemi.utils;

import java.util.ArrayList;
import java.util.List;

/**
 * PasswordDetails holds the list of PasswordDetail objects.
 * Created by munavar.basha on 11/13/2014.
 */
public class PasswordDetails {
    
    /** The p details. */
    private List<PasswordDetail> pDetails = null;

    /**
     * Instantiates a new password details.
     */
    public PasswordDetails(){
        pDetails = new ArrayList<PasswordDetail>();
    }

    /**
     * setPasswordDetail sets PasswordDetail Object.
     *
     * @param pDetail the new password detail
     */
    public void setPasswordDetail(PasswordDetail pDetail){
        pDetails.add(pDetail);
    }

    /**
     * getPasswordDetail gets ArrayList of PasswordDetail Objects.
     *
     * @return ArrayList the password details
     */
    public List getPasswordDetail(){
        return pDetails;
    }

}
