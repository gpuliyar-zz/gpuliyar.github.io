
package com.metricstream.systemi.rga.bean;

import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * Class which defines activation attributes.
 *
 * @author asif.u
 */
@XmlRootElement(name = "Activation")
public class ActivationBean {

	/** The Activation code. */
	private String	ActivationCode;
	
	/** The Activation code gen date. */
	private String	ActivationCodeGenDate;
	
	/** The Activation date. */
	private String	ActivationDate;
	
	/** The Activation expiry date. */
	private String	ActivationExpiryDate;
	
	/** The Activation status. */
	private String	ActivationStatus;// (Used, Expired)
    
    /** The queue name. */
    private String  queueName;
    
    /** The queue url. */
    private String  queueURL;

    /**
     * Gets the queue name.
     *
     * @return String
     */
    public String getQueueName() {
        return queueName;
    }

    /**
     * Sets the queue name.
     *
     * @param queueName the new queue name
     */
    @XmlElement(name = "QueueName")
    public void setQueueName(String queueName) {
        this.queueName = queueName;
    }

    /**
     * Gets the queue url.
     *
     * @return String
     */
    @XmlElement(name = "QueueURL")
    public String getQueueURL() {
        return queueURL;
    }

    /**
     * Sets the queue url.
     *
     * @param queueURL the new queue url
     */
    public void setQueueURL(String queueURL) {
        this.queueURL = queueURL;
    }

    /**
     * Gets the activation code.
     *
     * @return the activationCode
     */
	public String getActivationCode() {
		return ActivationCode;
	}
	
	/**
	 * Sets the activation code.
	 *
	 * @param activationCode the activationCode to set
	 */
	@XmlElement(name = "ActivationCode")
	public void setActivationCode(String activationCode) {
		ActivationCode = activationCode;
	}
	
	/**
	 * Gets the activation code gen date.
	 *
	 * @return the activationCodeGenDate
	 */
	public String getActivationCodeGenDate() {
		return ActivationCodeGenDate;
	}
	
	/**
	 * Sets the activation code gen date.
	 *
	 * @param activationCodeGenDate the activationCodeGenDate to set
	 */
	@XmlElement(name = "ActivationCodeGenDate")
	public void setActivationCodeGenDate(String activationCodeGenDate) {
		ActivationCodeGenDate = activationCodeGenDate;
	}
	
	/**
	 * Gets the activation date.
	 *
	 * @return the activationDate
	 */
	public String getActivationDate() {
		return ActivationDate;
	}

    /**
     * Sets the activation date.
     *
     * @param activationDate the new activation date
     */
	@XmlElement(name = "ActivationDate")
	public void setActivationDate(String activationDate) {
		ActivationDate = activationDate;
	}
	
	/**
	 * Gets the activation expiry date.
	 *
	 * @return the activationExpiryDate
	 */
	public String getActivationExpiryDate() {
		return ActivationExpiryDate;
	}
	
	/**
	 * Sets the activation expiry date.
	 *
	 * @param activationExpiryDate the activationExpiryDate to set
	 */
	@XmlElement(name = "ActivationExpiryDate")
	public void setActivationExpiryDate(String activationExpiryDate) {
		ActivationExpiryDate = activationExpiryDate;
	}
	
	/**
	 * Gets the activation status.
	 *
	 * @return the activationStatus
	 */
	public String getActivationStatus() {
		return ActivationStatus;
	}
	
	/**
	 * Sets the activation status.
	 *
	 * @param activationStatus the activationStatus to set
	 */
	@XmlElement(name = "ActivationStatus")
	public void setActivationStatus(String activationStatus) {
		ActivationStatus = activationStatus;
	}
}

