package com.metricstream.systemi.rga.domain;

/**
 * Class to represent information regarding a feed source.
 *
 * @author asif.u
 */
public class FeedInfo {
	
	/** The url. */
	String url;
	
	/** The user name. */
	String userName;
	
	/** The password. */
	String password;
	
	/** The parameter. */
	String parameter;
	
	/** The updated feed time stamp. */
	String updatedFeedTimeStamp;
	
	/** The SC infolet id. */
	String SCInfoletID;
	
	/** The channel id. */
	String channelId;
	
	/** The source id. */
	String sourceId;
	
	/** The email type. */
	String emailType;
	
	/** The save attachment. */
	String saveAttachment;
	
	/** The queue name. */
	String queueName;
	
	/** The queue url. */
	String queueURL;
	
	/** The type. */
	String type;
	
	
	
	/**
	 * Gets the url.
	 *
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}
	
	/**
	 * Sets the url.
	 *
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
	
	/**
	 * Gets the user name.
	 *
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	
	/**
	 * Sets the user name.
	 *
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	/**
	 * Gets the password.
	 *
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	
	/**
	 * Sets the password.
	 *
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	
	/**
	 * Gets the parameter.
	 *
	 * @return the parameter
	 */
	public String getParameter() {
		return parameter;
	}
	
	/**
	 * Sets the parameter.
	 *
	 * @param parameter the parameter to set
	 */
	public void setParameter(String parameter) {
		this.parameter = parameter;
	}
	
	/**
	 * Gets the updated feed time stamp.
	 *
	 * @return the updatedFeedTimeStamp
	 */
	public String getUpdatedFeedTimeStamp() {
		return updatedFeedTimeStamp;
	}
	
	/**
	 * Sets the updated feed time stamp.
	 *
	 * @param updatedFeedTimeStamp the updatedFeedTimeStamp to set
	 */
	public void setUpdatedFeedTimeStamp(String updatedFeedTimeStamp) {
		this.updatedFeedTimeStamp = updatedFeedTimeStamp;
	}
	
	/**
	 * Gets the SC infolet id.
	 *
	 * @return the sCInfoletID
	 */
	public String getSCInfoletID() {
		return SCInfoletID;
	}
	
	/**
	 * Sets the SC infolet id.
	 *
	 * @param sCInfoletID the sCInfoletID to set
	 */
	public void setSCInfoletID(String sCInfoletID) {
		SCInfoletID = sCInfoletID;
	}
	
	/**
	 * Gets the channel id.
	 *
	 * @return the channelId
	 */
	public String getChannelId() {
		return channelId;
	}
	
	/**
	 * Sets the channel id.
	 *
	 * @param channelId the channelId to set
	 */
	public void setChannelId(String channelId) {
		this.channelId = channelId;
	}
	
	/**
	 * Gets the source id.
	 *
	 * @return the sourceId
	 */
	public String getSourceId() {
		return sourceId;
	}
	
	/**
	 * Sets the source id.
	 *
	 * @param sourceId the sourceId to set
	 */
	public void setSourceId(String sourceId) {
		this.sourceId = sourceId;
	}
	
	/**
	 * Gets the save attachment.
	 *
	 * @return the saveAttachment
	 */
	public String getSaveAttachment() {
		return saveAttachment;
	}
	
	/**
	 * Sets the save attachment.
	 *
	 * @param saveAttachment the saveAttachment to set
	 */
	public void setSaveAttachment(String saveAttachment) {
		this.saveAttachment = saveAttachment;
	}
	
	/**
	 * Gets the queue name.
	 *
	 * @return the queueName
	 */
	public String getQueueName() {
		return queueName;
	}
	
	/**
	 * Sets the queue name.
	 *
	 * @param queueName the queueName to set
	 */
	public void setQueueName(String queueName) {
		this.queueName = queueName;
	}
	
	/**
	 * Gets the queue url.
	 *
	 * @return the queueURL
	 */
	public String getQueueURL() {
		return queueURL;
	}
	
	/**
	 * Sets the queue url.
	 *
	 * @param queueURL the queueURL to set
	 */
	public void setQueueURL(String queueURL) {
		this.queueURL = queueURL;
	}
	
	/**
	 * Gets the type.
	 *
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	
	/**
	 * Sets the type.
	 *
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * Gets the Email Type
	 * @return the emailType
	 */
	public String getEmailType() {
		return this.emailType;
	}

	/**
	 * Sets the Email Type
	 * @param emailType the emailType to set
	 */
	public void setEmailType(String emailType) {
		this.emailType = emailType;
	}
}
